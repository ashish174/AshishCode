package javaparactice;

/**
 * A-Z (65-90)
 * a-z (97-122)
 */
public class CharOperation {
    public static void main(String[] args) {
        char c = 65;
        char ch = 'c';
        char ch2 = 'e';
        int num = ch;
        System.out.println(c);
        System.out.println(num);
        System.out.println(ch2-ch);
    }
}
